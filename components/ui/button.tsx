import * as React from "react"
import { Slot } from "@radix-ui/react-slot"
import { cva, type VariantProps } from "class-variance-authority"
import { cn } from "@/lib/utils"

const buttonVariants = cva(
    // Base: radius 8px + comportamiento general
    "inline-flex items-center justify-center whitespace-nowrap rounded-lg text-sm font-medium ring-offset-background transition-all focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50 active:scale-[0.98]",
    {
        variants: {
            variant: {
                // ✅ Tikipal default (naranja)
                default:
                    "bg-[#FE6535] text-white shadow-sm hover:bg-[#e55a2f] hover:shadow-md",

                // ✅ Variante principal explícita (por si luego quieres cambiar default)
                tikipal:
                    "bg-[#FE6535] text-white shadow-md hover:bg-[#e55a2f] hover:shadow-lg",

                destructive:
                    "bg-destructive text-destructive-foreground hover:bg-destructive/90",

                outline:
                    "border border-input bg-background shadow-sm hover:bg-accent hover:text-accent-foreground hover:border-accent-foreground/20",

                secondary:
                    "bg-secondary text-secondary-foreground hover:bg-secondary/80",

                ghost: "hover:bg-accent hover:text-accent-foreground",

                link: "text-primary underline-offset-4 hover:underline",

                // ✅ CTA negro (como venías usando)
                premium:
                    "bg-gradient-to-r from-gray-900 to-black text-white shadow-lg hover:shadow-xl hover:-translate-y-0.5 border border-transparent",
            },

            size: {
                default: "h-10 px-5 py-2.5",
                // ✅ Mantener radius 8px también en sm (antes decía rounded-md)
                sm: "h-9 rounded-lg px-3",
                lg: "h-11 rounded-lg px-8 text-base",
                icon: "h-10 w-10",
            },
        },

        defaultVariants: {
            variant: "default",
            size: "default",
        },
    }
)

export interface ButtonProps
    extends React.ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {
    asChild?: boolean
}

const Button = React.forwardRef<HTMLButtonElement, ButtonProps>(
    ({ className, variant, size, asChild = false, ...props }, ref) => {
        const Comp = asChild ? Slot : "button"
        return (
            <Comp
                className={cn(buttonVariants({ variant, size, className }))}
                ref={ref}
                {...props}
            />
        )
    }
)

Button.displayName = "Button"

export { Button, buttonVariants }
